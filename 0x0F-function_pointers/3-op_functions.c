#include "3-calc.h"
/**
 * op_add - function to calcul sum of two numbers
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that calculate the difference
 * @a: first number
 * @b: second number
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that calculate the product
 * @a: first number
 * @b: second number
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that calculate the division
 * @a: first number
 * @b: second number
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that calculate the modulo
 * @a: first number
 * @b: second number
 * Return: modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
